#!/usr/bin/python3


def add(a, b):
    """Addition function

    Args:
        a: first integer
        b: second integer

    Returns:
        Return value. a + b
    """
    return (a + b)


def sub(a, b):
    """Subtraction function

    Args:
        a: first integer
        b: second integer

    Returns:
        Return value. a - b
    """
    return (a - b)


def mul(a, b):
    """Multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        Return value. a * b
    """
    return (a * b)


def div(a, b):
    """Division function

    Args:
        a: first integer
        b: second integer

    Returns:
        Return value. a / b
    """
    return int(a / b)
